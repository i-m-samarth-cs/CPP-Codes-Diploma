#include<iostream>
using namespace std;

int main()
{
	int a;
	//Accept
	cout<<"\n\n Enter Any Number: ";
	cin>>a;
	//Display
	if(a==0)
	{
		cout<<"\n\n Number Is Zero";
	}
	else if(a%2==0)
	{
		cout<<"\n\n Number is Even";
	}
	else
	{
		cout<<"\n\n Number is Even";
	}
	
	return 0;
}
